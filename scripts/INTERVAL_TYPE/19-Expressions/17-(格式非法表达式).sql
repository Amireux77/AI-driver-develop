!4 XG-TYPE-INTERVAL-EXPR-001 格式非法表达式 : 分隔符错误

{{{
    - 在 年月 格式中,分隔符限制为只能使用 '-',其余字符会报错
    - 在 天时分秒 格式中,天与时分秒之间有分隔符,在插入数值带特殊符号时,会将特殊符号之前的内容识别为天,之后的内容识别为时分秒,特殊符号会被识别为分隔符
    - interval hour 格式特殊,走了 day to hour 格式,因此在这里插入分隔符也是可以通过的
}}}

SELECT '1,0'::INTERVAL YEAR;
SELECT '1/0'::INTERVAL MONTH;
SELECT '1|0'::INTERVAL DAY;
SELECT '1,0'::INTERVAL HOUR;
SELECT '1+0'::INTERVAL MINUTE;
SELECT '1@0'::INTERVAL SECOND;
SELECT '1.2.3'::INTERVAL YEAR TO MONTH;
SELECT '1|0'::INTERVAL DAY TO HOUR;
SELECT '1;0:0'::INTERVAL DAY TO MINUTE;
SELECT '1|0:0:0'::INTERVAL DAY TO SECOND;
SELECT '1|0'::INTERVAL HOUR TO MINUTE;
SELECT '1|0:0'::INTERVAL HOUR TO SECOND;
SELECT '1|0.0'::INTERVAL MINUTE TO SECOND;

