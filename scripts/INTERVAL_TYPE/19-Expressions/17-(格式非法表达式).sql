!4 XG-TYPE-INTERVAL-EXPR-004 格式非法表达式 : 多余非法字符

{{{ 当输入值不为纯数值格式时,会自动走标准字面量格式,因此特殊符号会被识别为分隔符,不会报特殊字符的错误,而其余非合法字母等字符会被报错}}}

SELECT '1-@x'::INTERVAL YEAR;
SELECT '1#'::INTERVAL MONTH;
SELECT '1$'::INTERVAL DAY;
SELECT '1%'::INTERVAL HOUR;
SELECT '1&'::INTERVAL MINUTE;
SELECT '1%'::INTERVAL SECOND;
SELECT '1-0x'::INTERVAL YEAR TO MONTH;
SELECT '1 0q'::INTERVAL DAY TO HOUR;
SELECT '1 0:0@'::INTERVAL DAY TO MINUTE;
SELECT '1 0:0:0!'::INTERVAL DAY TO SECOND;
SELECT '1:0~'::INTERVAL HOUR TO MINUTE;
SELECT '1:0:0}'::INTERVAL HOUR TO SECOND;
SELECT '1:0.0['::INTERVAL MINUTE TO SECOND;

