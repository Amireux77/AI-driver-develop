{{{
    XuguDB 支持通过 INTERVAL_STYLE 系统参数切换输出格式,默认输出格式为 SQL_STANDARD 标准
    目前支持以下4种格式 : SQL_STANDARD (0) 、ISO_8601 (1) 、POSTGRES (2) 、POSTGRES_VERBOSE (3)
    输出结果会按照设置格式显示
}}}

!4 XG-TYPE-INTERVAL-Output_Style-001 测试 SQL_STANDARD 输出格式
<预期结果 : 1-0>
SELECT INTERVAL '1-0' YEAR;
<预期结果 : 0-1>
SELECT INTERVAL '0-1' MONTH;
<预期结果 : 12 0:00:00>
SELECT INTERVAL '12 0:00:00' DAY;
<预期结果 : 12:00:00>
SELECT INTERVAL '12:00:00' HOUR;
<预期结果 : 0:12:00>
SELECT INTERVAL '0:12:00' MINUTE;
<预期结果 : 0:00:12>
SELECT INTERVAL '0:00:12' SECOND;
<预期结果 : 1-0>
SELECT INTERVAL '1-0' YEAR TO MONTH;
<预期结果 : 12:00:00>
SELECT INTERVAL '12:00:00' DAY TO HOUR;
<预期结果 : 0:12:00>
SELECT INTERVAL '00:12:00' DAY TO MINUTE;
<预期结果 : 0:00:12>
SELECT INTERVAL '00:00:12' DAY TO SECOND;
<预期结果 : 0:12:00>
SELECT INTERVAL '00:12:00' HOUR TO MINUTE;
<预期结果 : 0:00:12>
SELECT INTERVAL '00:00:12' HOUR TO SECOND;
<预期结果 : 0:00:12>
SELECT INTERVAL '00:00:12' MINUTE TO SECOND;

